package operators;

public class LogicalOperators {
	public void LogicalAndOperator(int a,int b){
		if (a==5 && b==4);
		System.out.println("Value of a:" +a+ "Value of b:" +b);
	
		
	
	}
		
	public void OrOperators(int a,int b){
		if(a==6||b==4);
		System.out.println("Value of a:" +a+ "Value of b:" +b);
	}

	public void NotOperators(int a){
		if (!(a==7));
		System.out.println("Value of a:"+a);
		
	}
	public static void main(String[] args){
		LogicalOperators obj = new LogicalOperators();
		obj.LogicalAndOperator(4,6);
		obj. OrOperators(6,4);
		obj.NotOperators(8);
		
		
		}
		
}

	
	
	
	
	
	
	
	
	
	
	
	
		

	
	
	
	
	
	
	


	        
	        
			
			
			
			
			
			
			
			
			
		
			
		
			
		
		
		
	

				
				
				
			
		
	
	
			
		

	
